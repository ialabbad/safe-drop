export type LanguageKey = 'ar' | 'en';

export const useTranslation = (language: LanguageKey) => {
  return (key: string) => {
    if (!translations[key]) {
      console.warn(`Translation for key "${key}" not found`);
      return key;
    }
    return translations[key][language];
  };
};
